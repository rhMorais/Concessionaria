CREATE PROCEDURE INSERIR_CLIENTE
@CPF VARCHAR(12), @NOME VARCHAR(75), @ENDERECO VARCHAR(75), 
@TELEFONE VARCHAR(13),@DATANASC DATE, @CIDADE VARCHAR(30)
AS
BEGIN 
	INSERT INTO CLIENTE(CLICPF, CLINOME, CLIENDER, CLITELEF, CLIDATAN, CLICIDAD)
	VALUES (@CPF, @NOME, @ENDERECO, @TELEFONE, @DATANASC, @CIDADE)
END
