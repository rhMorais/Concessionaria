CREATE PROCEDURE INSERIR_LOGIN
@USER VARCHAR(12), @SENHA VARCHAR(12)
AS
BEGIN 
	INSERT INTO LOGIN_VENDEDOR(LOGUSUAR, LOGSENHA)
	VALUES (@USER, @SENHA)
