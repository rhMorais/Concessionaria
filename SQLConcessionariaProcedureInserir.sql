CREATE PROCEDURE INSERIR_CARRO
@MODELO VARCHAR(50), @MARCA VARCHAR(20), @ANO VARCHAR(20), 
@TIPO VARCHAR(20), @COMBUSTIVEL VARCHAR(30), @COR VARCHAR(30)
AS
BEGIN 
	INSERT INTO CARRO (CARMODEL, CARMARCA, CARANO, CARTIPO, CARCOMBU, CARCOR)
	VALUES (@MODELO, @MARCA, @ANO, @TIPO, @COMBUSTIVEL, @COR)
END
