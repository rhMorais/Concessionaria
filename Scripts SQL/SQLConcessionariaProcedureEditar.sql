CREATE PROCEDURE EDITAR_OPCIONAL
	@OPCID INT, 
	@OPCDESCR VARCHAR(50)
	
	AS
	BEGIN 
		UPDATE OPCIONAL SET OPCDESCR = @OPCDESCR
		WHERE OPCID = @OPCID
	END
