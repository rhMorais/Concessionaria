ALTER PROCEDURE EDITAR_CARRO
	@CARPLACA VARCHAR(8), 
	@CARMODEL VARCHAR(30), 
	@CARMARCA VARCHAR(20), 
	@CARCOR VARCHAR(30), 
	@CARCOMBU VARCHAR(20) = NULL, 
	@CARTIPO VARCHAR(20) = NULL, 
	@CARANO VARCHAR(20)
	
	AS
	BEGIN 
		UPDATE CARRO 
			SET CARMODEL = @CARMODEL, 
				CARMARCA = @CARMARCA, 
				CARCOR	= @CARCOR,
				CARCOMBU = @CARCOMBU, 
				CARTIPO = @CARTIPO, 
				CARANO = @CARANO
		WHERE CARPLACA = @CARPLACA
	END
