CREATE PROCEDURE EDITAR_LOGIN
	@LOGUSER VARCHAR(12), 
	@LOGSENHA VARCHAR(12)

	AS
	BEGIN 
		UPDATE LOGIN_VENDEDOR 
			SET LOGSENHA = @LOGSENHA
		WHERE LOGUSUAR = @LOGUSER
	END
